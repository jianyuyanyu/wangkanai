namespace Wangkanai.Tabler.Models;

/// <summary>
/// Defines the size variants for Tabler icon components.
/// Maps to Tabler CSS icon size classes.
/// </summary>
public enum IconSize
{
	/// <summary>
	/// Default icon size (16px, no size class applied)
	/// </summary>
	Default = 0,
	
	/// <summary>
	/// Extra small icon size (icon-xs, 12px)
	/// </summary>
	ExtraSmall,
	
	/// <summary>
	/// Small icon size (icon-sm, 14px)
	/// </summary>
	Small,
	
	/// <summary>
	/// Medium icon size (icon-md, 18px)
	/// </summary>
	Medium,
	
	/// <summary>
	/// Large icon size (icon-lg, 20px)
	/// </summary>
	Large,
	
	/// <summary>
	/// Extra large icon size (icon-xl, 24px)
	/// </summary>
	ExtraLarge
}