namespace Wangkanai.Tabler.Models;

/// <summary>
/// Defines the size variants for Tabler button components.
/// Maps to Tabler CSS button size classes.
/// </summary>
public enum ButtonSize
{
	/// <summary>
	/// Medium/default button size (no size class applied)
	/// </summary>
	Medium = 0,
	
	/// <summary>
	/// Small button size (btn-sm)
	/// </summary>
	Small,
	
	/// <summary>
	/// Large button size (btn-lg)
	/// </summary>
	Large
}